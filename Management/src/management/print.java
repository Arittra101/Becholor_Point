    public void printRecord(JPanel panel) {
        PrinterJob pj = PrinterJob.getPrinterJob();
        pj.setJobName("printter record");

        pj.setPrintable(new Printable() {

            @Override
            public int print(Graphics graphics, PageFormat pf, int i) throws PrinterException {

                //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
                if (i > 0) {
                    return Printable.NO_SUCH_PAGE;
                }

                Graphics2D graphics2D = (Graphics2D) graphics;
                graphics2D.translate(pf.getImageableX() * 2, pf.getImageableY() * 2);
                graphics2D.scale(0.5, 0.5);
                panel.paint((graphics2D));
                return Printable.PAGE_EXISTS;

            }

        });
            
            boolean returnResult = pj.printDialog();
            
            if(returnResult)
            {
                try{
                    pj.print();
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
    }

